import pytest
from C import solve


def test_solve():
    assert solve('3\n1\n2 1\n1\n1 1\n1\n2 0') == '2'
    assert solve('3\n2\n2 1\n3 0\n2\n3 1\n1 0\n2\n1 1\n2 0') == '0'
    assert solve('2\n1\n2 0\n1\n1 0') == '1'
    assert solve('1\n0') == '1'
    assert solve('2\n0\n0') == '2'
    assert solve('2\n1\n2 1\n0') == '2'
    assert solve('2\n1\n2 0\n0') == '1'
    assert solve('2\n0\n1\n1 1') == '2'
    assert solve('2\n0\n1\n1 0') == '1'
    assert solve('2\n1\n2 1\n1\n1 1') == '2'
    assert solve('2\n1\n2 1\n1\n1 0') == '1'
    assert solve('2\n1\n2 0\n1\n1 1') == '1'
    assert solve('2\n1\n2 0\n1\n1 0') == '1'
    assert solve('3\n0\n0\n0') == '3'
    assert solve('3\n1\n2 1\n0\n0') == '3'
    assert solve('3\n1\n2 0\n0\n0') == '2'
    assert solve('3\n1\n3 1\n0\n0') == '3'
    assert solve('3\n1\n3 0\n0\n0') == '2'
    assert solve('3\n0\n1\n1 1\n0') == '3'
    assert solve('3\n0\n1\n1 0\n0') == '2'
    assert solve('3\n0\n1\n3 1\n0') == '3'
    assert solve('3\n0\n1\n3 0\n0') == '2'
    assert solve('3\n0\n0\n1\n1 1') == '3'
    assert solve('3\n0\n0\n1\n1 0') == '2'
    assert solve('3\n0\n0\n1\n2 1') == '3'
    assert solve('3\n0\n0\n1\n2 0') == '2'
    assert solve('3\n2\n2 1\n3 1\n0\n0') == '3'
    assert solve('3\n2\n2 0\n3 1\n0\n0') == '2'
    assert solve('3\n2\n2 1\n3 0\n0\n0') == '2'
    assert solve('3\n2\n2 0\n3 0\n0\n0') == '2'
    assert solve('3\n0\n2\n1 1\n3 1\n0') == '3'
    assert solve('3\n0\n2\n1 0\n3 1\n0') == '2'
    assert solve('3\n0\n2\n1 1\n3 0\n0') == '2'
    assert solve('3\n0\n2\n1 0\n3 0\n0') == '2'
    assert solve('3\n0\n0\n2\n1 1\n2 1') == '3'
    assert solve('3\n0\n0\n2\n1 0\n2 1') == '2'
    assert solve('3\n0\n0\n2\n1 1\n2 0') == '2'
    assert solve('3\n0\n0\n2\n1 0\n2 0') == '2'
    assert solve('3\n1\n2 1\n1\n3 1\n0') == '3'
    assert solve('3\n1\n2 0\n1\n3 1\n0') == '2'
    assert solve('3\n1\n2 1\n1\n3 0\n0') == '2'
    assert solve('3\n1\n2 0\n1\n3 0\n0') == '2'
    assert solve('3\n1\n2 1\n1\n1 1\n0') == '3'
    assert solve('3\n1\n2 0\n1\n1 1\n0') == '2'
    assert solve('3\n1\n2 1\n1\n1 0\n0') == '2'
    assert solve('3\n1\n2 0\n1\n1 0\n0') == '2'
    assert solve('3\n1\n3 1\n1\n3 1\n0') == '3'
    assert solve('3\n1\n3 0\n1\n3 1\n0') == '2'
    assert solve('3\n1\n3 1\n1\n3 0\n0') == '2'
    assert solve('3\n1\n3 0\n1\n3 0\n0') == '2'
    assert solve('3\n1\n3 1\n1\n1 1\n0') == '3'
    assert solve('3\n1\n3 0\n1\n1 1\n0') == '2'
    assert solve('3\n1\n3 1\n1\n1 0\n0') == '2'
    assert solve('3\n1\n3 0\n1\n1 0\n0') == '2'
    assert solve('3\n0\n1\n3 1\n1\n1 1') == '3'
    assert solve('3\n1\n2 1\n0\n1\n1 1') == '3'
